package com.anthonypse.androidwidgetsdemo;

/**
 * Created by anthonypse@gmail.com
 *
 * These strings define various content types.  Mostly used in this app for filtering items from
 * the file selection dialogs.  i.e. only selecting audio files for the ringtone or only .png files
 * for wallpaper
 */
public class MimeContentType {
    public static final String AUDIO = "audio/*";
    public static final String AUDIO_MP3 = "audio/mpeg";
    public static final String IMG = "image/*";
    public static final String IMG_PNG = "image/png";
}
